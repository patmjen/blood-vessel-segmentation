class BCELoss(nn.Module):
    def __init__(self):
        super().__init__()

    def forward(self, input, target):
        return F.binary_cross_entropy_with_logits(input, target)


class DiceLoss(nn.Module):
    def __init__(self):
        super().__init__()

    def forward(self, input, target, smooth=1e-6):
        iflat = torch.sigmoid(input).view(input.size(0), -1)
        tflat = target.view(target.size(0), -1)
        intersection = torch.sum(iflat * tflat)
        dice = 1.0 - (2.0 * intersection + smooth)/(torch.sum(iflat + tflat) + smooth)
        return dice


class BCEDiceLoss(nn.Module):
    def __init__(self):
        super().__init__()

    def forward(self, input, target, smooth=1e-5):
        bce = F.binary_cross_entropy_with_logits(input, target)
        input = torch.sigmoid(input)
        num = target.size(0)
        input = input.view(num, -1)
        target = target.view(num, -1)
        if (input == 1.0).any():
            intersection = (input * target)
            dice = (2. * intersection.sum(1) + smooth) / (input.sum(1) + target.sum(1) + smooth)
            dice = 1 - dice.sum() / num
            return 0.5 * bce + dice
        else:
            return bce


def binary_dice_coefficient(pred: torch.Tensor, gt: torch.Tensor,
                            thresh: float = 0.5, smooth: float = 1e-4) -> torch.Tensor:

    pred = torch.sigmoid(pred)
    pred_flat = pred.view(pred.size(0), -1)
    gt_flat = gt.view(gt.size(0), -1)

    pred_bool = pred_flat > thresh

    intersec = (pred_bool * gt_flat).float()
    return 2 * intersec.sum() / (pred_bool.float().sum()
                                 + gt_flat.float().sum() + smooth)

