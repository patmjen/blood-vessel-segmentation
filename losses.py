def isnan(x):
    return x != x


def mean(data, ignore_nan=False, empty=0):
    """
    nanmean compatible with generators.
    """
    data = iter(data)
    if ignore_nan:
        data = ifilterfalse(isnan, data)
    try:
        n = 1
        acc = next(data)
    except StopIteration:
        if empty == 'raise':
            raise ValueError('Empty mean')
        return empty
    for n, v in enumerate(data, 2):
        acc += v
    if n == 1:
        return acc
    return acc / n
